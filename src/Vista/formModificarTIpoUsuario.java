    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            int id = Integer.parseInt(lblIDTipoUsuario.getText());
            PreparedStatement ps = cn.prepareStatement("update tiposusuario set sueldo=? where id_tipoU=? ");
            ps.setDouble(1, Double.parseDouble(txtSueldo.getText()));
            ps.setInt(2, id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Modificaci√≥n exitosa", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            tiposUsuario tu = new tiposUsuario();
            tu.setVisible(true);
        } catch (SQLException e) {
            System.out.println("Error al actualizar los productos en la BD " + e);
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error en el formato de n√∫mero: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }        
    }//GEN-LAST:event_btnModificarActionPerformed

